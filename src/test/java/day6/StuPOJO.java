package day6;

public class StuPOJO {
	
	int StudentId;
	String StudentName;
	String StudentDetp;
	String [] Courses;
			 
	
	public int getStudentId() {
		return StudentId;
	}
	public void setStudentId(int studentId) {
		StudentId = studentId;
	}
	public String getStudentName() {
		return StudentName;
	}
	public void setStudentName(String studentName) {
		StudentName = studentName;
	}
	public String getStudentDetp() {
		return StudentDetp;
	}
	public void setStudentDetp(String studentDetp) {
		StudentDetp = studentDetp;
	}
	public String[] getCourses() {
		return Courses;
	}
	public void setCourses(String[] courses) {
		Courses = courses;
	}


}
